#arithmetic

x = 9
y = 4

z = x + y
print('Addition of x and y: ', z)

z = x - y
print('Subtraction of x and y :', z)

z = x / y
print(' Division of x by y :', z)


z = x // y
print('Floor division: ', z)

z = x % y
print('Modulus or reminder :', z)

z = x ** y
print('Exponent of x and y : ', z)